import { useState, useEffect } from 'react';

export type Lang = 'it' | 'en' | 'es' | 'fr' | 'de' | 'pt' | 'ja' | 'zh' | 'ar' | 'ru' | 'ko';

const STORAGE_KEY = '@jes_lang';

// Module-level current language (synchronous reads after init)
let _lang: Lang = 'it';

// Lazy-load AsyncStorage to avoid hard dependency
async function getStorage() {
  try {
    // eslint-disable-next-line @typescript-eslint/no-var-requires
    return require('@react-native-async-storage/async-storage').default;
  } catch { return null; }
}

export function getLang(): Lang { return _lang; }

export async function initLang(): Promise<void> {
  try {
    const AS = await getStorage();
    if (!AS) return;
    const saved = await AS.getItem(STORAGE_KEY);
    if (saved && T[saved as Lang]) _lang = saved as Lang;
  } catch {}
}

export async function setLang(lang: Lang): Promise<void> {
  _lang = lang;
  try {
    const AS = await getStorage();
    if (AS) await AS.setItem(STORAGE_KEY, lang);
  } catch {}
}

export function t(key: string): string {
  return T[_lang]?.[key] ?? T['it']?.[key] ?? key;
}

// Hook for components that need to re-render on language change
const _listeners: Set<() => void> = new Set();
export function useLang(): [Lang, (l: Lang) => void] {
  const [lang, setLangState] = useState<Lang>(_lang);
  useEffect(() => {
    const handler = () => setLangState(_lang);
    _listeners.add(handler);
    return () => { _listeners.delete(handler); };
  }, []);
  const change = async (l: Lang) => {
    await setLang(l);
    _listeners.forEach(fn => fn());
  };
  return [lang, change];
}


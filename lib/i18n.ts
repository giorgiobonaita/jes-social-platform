const STORAGE_KEY = 'jes_lang';

function getInitialLang(): Lang {
  if (typeof window === 'undefined') return 'it';
  const saved = localStorage.getItem(STORAGE_KEY) as Lang | null;
  if (saved && T[saved]) return saved;
  const browser = navigator.language.split('-')[0] as Lang;
  if (T[browser]) return browser;
  return 'it';
}

// Module-level subscribers so all components sync instantly
const subscribers = new Set<() => void>();
let currentLang: Lang = 'it';

export function setLang(lang: Lang) {
  currentLang = lang;
  if (typeof window !== 'undefined') localStorage.setItem(STORAGE_KEY, lang);
  subscribers.forEach(fn => fn());
}

export function useLang(): { lang: Lang; t: (key: string) => string; setLang: (l: Lang) => void } {
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window !== 'undefined' && currentLang === 'it') {
      currentLang = getInitialLang();
    }
    return currentLang;
  });

  useEffect(() => {
    const update = () => setLangState(currentLang);
    subscribers.add(update);
    return () => { subscribers.delete(update); };
  }, []);

  const t = useCallback((key: string) => {
    return T[lang]?.[key] ?? T['it']?.[key] ?? key;
  }, [lang]);

  return { lang, t, setLang };
}
